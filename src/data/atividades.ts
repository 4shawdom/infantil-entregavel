export function getAtividadesParaNivel(nivel: number): Atividade[] {
  return ATIVIDADES.filter((a) => a.nivel_min <= nivel && a.nivel_max >= nivel);
}

export function getAtividadesParaRotina(nivel: number, habilidadeFoco: Habilidade | null): Atividade[] {
  const pool = getAtividadesParaNivel(nivel);
  if (!habilidadeFoco) return pool.slice(0, 3);
  const foco = pool.filter((a) => a.habilidade === habilidadeFoco);
  const outras = pool.filter((a) => a.habilidade !== habilidadeFoco);
  return [...foco.slice(0, 2), ...outras.slice(0, 1)];
}
